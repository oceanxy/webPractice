/**
 * @param {number[]} arr
 * @param {number} k
 * @return {number[]}
 */
var getLeastNumbers = function(arr, k) {
  if(k < 0 || k > arr.length || arr.length > 10000) {
    return [];
  }

  return arr.sort((a, b) => a - b).slice(0, k); // 既然是算法，不推荐直接用内置函数。详细内容见readme
};

